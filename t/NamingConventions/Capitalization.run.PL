#-----------------------------------------------------------------------------

## name PPI misparses part of ternary as a label (RT #41170)
## failures 0
## cut

my $foo = $condition ? $objection->method : $alternative;
my $foo = $condition ? undef : 1;

END_SIMPLE_TESTS

    return;
}


